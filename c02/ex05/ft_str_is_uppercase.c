int	ft_str_is_uppercase(char *str)
{
	int result;

	result = 1;
	while (*str != '\0')
	{
		if (!(*str >= 'A' && *str <= 'Z'))
		{
			result = 0;
		}
		str++;
	}
	return (result);
}
