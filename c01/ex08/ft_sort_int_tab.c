void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int x;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size - 1 - i)
		{
			if (*(tab + j) > *(tab + j + 1))
			{
				x = *(tab + j + 1);
				*(tab + j + 1) = *(tab + j);
				*(tab + j) = x;
			}
			j++;
		}
		i++;
	}
}
